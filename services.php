            Ce site est accessible en intranet, mais aussi depuis l'internet.<br />
            Les réservations payantes sont facturées par la Région aux utilisateurs.<br />
            L’administration de la M2L lui communique les informations nécessaires à cette facturation de façon hebdomadaire.
        </p> <br />


        <h2 id="Intégration_postes_informatiques">Intégration des postes informatiques des ligues</h2>

        <p> Lorsque les ligues acquièrent du matériel informatique, il y a une phase obligatoire d'intégration qui consiste à :
        <ul>
            <li>installer un antivirus affilié au serveur antiviral de la M2L,</li>
            <li>installer la dernière version de l'agent qui réalise l’inventaire matériel et logiciel</li>
            <li>paramétrer le poste en adressage IP automatique,</li>
            <li>installer un système de sauvegarde de données sur un site FTP de sauvegarde géré par la M2L,</li>
            <li>effectuer les dernières mises à jour systèmes et à paramétrer leur automatisation,</li>
            <li>paramétrer les noms des postes selon les règles de gestion suivantes :</li>

            <ol> <!--debut de liste ordonée-->
                <li>B[code bâtiment] : Code bâtiment qui peut être A ou C</li>
                <li>E[numéro étage]: N° étage est compris entre 1 et 4 (puisque les locaux du rez-de-chaussée n'hébergent pas de ligues)</li>
                <li>L[numéro ligue]: N° ligue sur 2 chiffres : correspond à un nombre attribué à la ligue allant pour l'instant de 01 à 24</li>
                <li>S[numéro salle]: N° salle sur 2 chiffres : correspond aux bureaux occupés par les ligues.</li>
                <li>P[numéro poste]: N° poste sur 2 chiffres : correspond au numéro écrit sur la prise murale</li>
            </ol> <!--fin de liste ordonée-->

        </ul>
        </p><br />



        <p class="dessous">Exemple : le nom d'hôte BAE2L06S01P01 correspond au poste installé sur la prise N°1 du bureau A201 occupé par la ligue de Volley, bureau situé au deuxième étage du bâtiment A. <br />
            Cette intégration est contractualisée.<br />
            Les ligues et CD étant toutes des structures associatives indépendantes, leurs postes ne sont pas intégrés dans un annuaire central.<br />
            Par contre, les postes de l'administration de la M2L et de la salle multimédia le sont.
        </p> <br/>


        <h2 id="Intégration_imprimantes">Intégration d'imprimantes</h2>

        <p> Lorsque les structures hébergées s'équipent d'imprimante réseau, la connexion en est réalisée par l'informaticien bénévole du CROSL, sans passer par un serveur d'impression.<br />
            Le nom de l'imprimante est lui aussi codifié de la même façon que celui des postes (la lettre L vient remplacer la lettre P).
        </p> <br/>

        <h2 id="Service_bulletins_salaire">Service d'établissement de bulletins de salaire </h2>

        <p> Le CRIB (Centre Régional d'Information des Bénévoles) est un label donné au CROSL qui, entre autres missions d'information (sur les textes réglementaires, la convention nationale du sport ...), propose un service d'établissement de bulletins de salaires aux ligues et à leurs clubs affiliés.<br />
            Le CROSL est tiers de confiance pour l'URSSAF et, à ce titre, établit des bulletins de salaires réglementaires et tous les documents annexes.<br />
            La prestation est facturée au forfait (60 € par an) et au bulletin édité (5 €).<br />
            Un employé du CROSL est affecté à cette mission à raison de 0,8 ETP.</p>


        <p>Illustration du processus pour une ligue qui fait établir ses bulletins de salaires par le CRIB, pour des animateurs à la vacation, comme pour ses employés permanents.</p><br />
        <ul>
            <li> La ligue établit une « déclaration unique d'embauche » et un contrat de travail. </li> <br />

            <li>Les données "salaires" sont envoyées par les associations au CRIB avant le 15 du mois :</li> <br />
            <li>Nom, prénom, date de naissance, adresse, n° de sécu, de l’intervenant ou du salarié </li>  <br />
            <li>Dates et heures d'intervention (de jour / de nuit) </li> <br />
            <li>Intitulé dans la grille de la convention nationale du sport (exemple : technicien niveau 3) </li> <br />
        </ul>

        <p class="dessous">En retour, le CRIB fournit le bulletin de salaire et le document sur les charges périodiques.<br />

            Le salaire est viré par la ligue, si celle-ci a fait ce choix. Sinon il y a un prélèvement sur son compte et virement sur le compte du salarié.

            Pour les versements aux organismes sociaux, il y a prélèvement direct sur le compte de la ligue.
        </p> <br />

        <h2 class="dessous" id="Formations">Formations</h2>


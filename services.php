        <h2 id="Affranchissement">Affranchissement</h2>

        <p> Une machine à affranchir permet un affranchissement rapide et
            en nombre.<br /> Cette prestation est facturée aux ligues au coût de
            l'affranchissement.<br />
            Chaque mois, on relie la machine à affranchir à une imprimante pour obtenir une liste de codes de gestion correspondant aux ligues associés à une quantité et un type d'affranchissement. <br />
            La prise en compte de ces informations permet au CROSL d'éditer des factures.
        </p> <br />

        <h2 id="Impressions_imprimerie">Impressions en volume et en qualité imprimerie</h2>

        <p> Les ligues disposent de la possibilité d'imprimer sur des ressources
            d'impression numériques connectées situées dans le local reprographie du
            rez-de-chaussée dont l'usage fait l'objet d'une facturation à prix coûtant. <br />
            Un système de comptage situé sur le serveur d'impression permet au CROSL d’effectuer une facturation mensuelle auprès des ligues.

        <ul>
            <li>une photocopieuse noir et blanc à 70 pages/minute avec différents dispositifs de finition, </li>
            <li>une imprimante Laser couleur A4/A3 à encre solide à 25 pages/minute,</li>
            <li>un traceur A2 (1 page / minute) utilisé pour les affiches et banderoles.</li>
        </ul>
        <br /><br /><br />

        <h2 class="dessous" id="Serveur_FTP_documentaire" >Serveur FTP documentaire</h2>

        <p> La M2L met à disposition des ligues un serveur FTP documentaire intranet/internet regroupant des textes légaux, des modèles de dossiers, de statuts, des programmes de formation (...) compilés par le CROSL.
        </p> <br />

        <h2 id="Système_réservation">Système de réservation des salles</h2>


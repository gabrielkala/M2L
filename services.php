        <h2 id="Téléphonie">Téléphonie</h2>

        <p> Dans les bâtiments anciens A et B, les salles et bureaux sont équipés
            de prises de téléphone analogiques. <br/>
            La M2L y fournit les combinés
            téléphoniques. <br/>
            Dans les bâtiments neufs C et D, l'équipement téléphonique
            est de type VoIP. <br/>
            La M2L loue des postes téléphoniques IP aux ligues.
